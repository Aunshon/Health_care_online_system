    private void change() {
       
        try {
            String query="update tbl_patient set P_password='"+P_password_tf.getText()+"' WHERE P_id='"+P_id_tf.getText()+"' and P_username='"+P_username_tf.getText()+"' and P_email='"+P_email_tf.getText()+"' and P_color='"+P_color_tf.getText()+"'";
            pst=conn.prepareStatement(query);
            pst.execute();
            pst.close();
            JOptionPane.showMessageDialog(null, "Password Changed Successfully");
            new Patient_Login().setVisible(true);
            dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    private void congrem_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_congrem_btnActionPerformed
        String username=P_username_tf.getText().toString();
        String password=P_password_tf_n.getText().toString();
        String email=P_email_tf.getText().toString();
        String dep=P_color_tf.getText().toString();
        String cpassword=P_password_tf.getText().toString();
        
        if(username.isEmpty()||password.isEmpty()||email.isEmpty()||dep.isEmpty()||cpassword.isEmpty()
                ||password == null ? cpassword != null : !password.equals(cpassword)
                ){
            JOptionPane.showMessageDialog(null, "Please Fill Up All The Box Or Check The Password");
        }
        else{
                try {
                    String a="select * from tbl_patient where P_id='"+P_id_tf.getText()+"' and P_username='"+P_username_tf.getText()+"' and P_email='"+P_email_tf.getText()+"' and P_color='"+P_color_tf.getText()+"'";
                    pst=conn.prepareStatement(a);
                    rst=pst.executeQuery();
                    
                if(rst.next()){
                    change();
                }
                else{
                    
                    JOptionPane.showMessageDialog(null, "Your Information Doesn't Matches");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
             }
    }//GEN-LAST:event_congrem_btnActionPerformed

