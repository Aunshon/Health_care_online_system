    private void send_message(){
        try {
            String query=" insert into tbl_message(email,message) values (?,?)";
            pst = conn.prepareStatement(query);
            
            pst.setString(1, P_email_tf.getText());
            pst.setString(2, message_box_tf.getText());
            
            pst.execute();
            pst.close();
            JOptionPane.showMessageDialog(null, "Message Sent Successfully");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
