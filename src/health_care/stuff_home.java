    public void refresh(){
        
        
        try {
            String query="select * from tbl_stuff_application where S_id='"+id.getText()+"' and S_username='"+username.getText()+"' and S_email='"+email.getText()+"'";
            pst=conn.prepareStatement(query);
            rs=pst.executeQuery();
            
            table.setModel(DbUtils.resultSetToTableModel(rs));
            pst.close();
            rs.close();
            msg.setText("");
            old_password.setText("");
            S_password_tf_n.setText("");
            c_password_tf.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
