    private void change() {
       
        try {
            String query="update tbl_Stuff set S_password='"+c_password_tf.getText()+"' WHERE S_id='"+id.getText()+"' and S_username='"+username.getText()+"' and S_email='"+email.getText()+"'";
            pst=conn.prepareStatement(query);
            pst.execute();
            pst.close();
            JOptionPane.showMessageDialog(null, "Password Changed Successfully");
            new Stuff_Login().setVisible(true);
            dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    private void congrem_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_congrem_btnActionPerformed
        String user=username.getText().toString();
        String pass=S_password_tf_n.getText().toString();
        String mail=email.getText().toString();
        String cpassword=c_password_tf.getText().toString();

        if(user.isEmpty()||pass.isEmpty()||mail.isEmpty()||cpassword.isEmpty()
            ||pass == null ? cpassword != null : !pass.equals(cpassword)
        ){
            JOptionPane.showMessageDialog(null, "Please Fill Up All The Box Or Check The Password");
        }
        else{
            try {
                String a="select * from tbl_stuff where S_id='"+id.getText()+"' and S_username='"+username.getText()+"' and S_email='"+email.getText()+"'";
                pst=conn.prepareStatement(a);
                rs=pst.executeQuery();

                if(rs.next()){
                    change();
                }
                else{

                    JOptionPane.showMessageDialog(null, "Your Information Doesn't Matches");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_congrem_btnActionPerformed

